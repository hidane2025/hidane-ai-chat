# CONTEXT.mdのパス（Render上では環境変数で指定可能）
CONTEXT_PATH = os.environ.get(
    "CONTEXT_MD_PATH",
    str(Path(__file__).parent.parent / "CONTEXT.md"),
)


def load_context() -> str:
    """CONTEXT.mdを読み込む。ファイルがなければ空文字を返す。"""
    try:
        with open(CONTEXT_PATH, "r", encoding="utf-8") as f:
            return f.read()
    except FileNotFoundError:
        return ""


def _extract_section(text: str, header: str) -> str:
    """Markdownセクションを抽出する（## ■ ヘッダー 〜 次の ## ■ まで）"""
    start = text.find(header)
    if start == -1:
        return ""
    next_section = text.find("\n## ■", start + len(header))
    if next_section == -1:
        return text[start:].strip()
    return text[start:next_section].strip()


